package meet.beans;

public class meet {
	private int m_no;
	private String m_place;
	private String m_date;
	private int m_people;
	private int m_max;
	private String image;

	public int getM_no() {
		return m_no;
	}
	public void setM_no(int m_no) {
		this.m_no = m_no;
	}
	public String getM_place() {
		return m_place;
	}
	public void setM_place(String m_place) {
		this.m_place = m_place;
	}
	public String getM_date() {
		return m_date;
	}
	public void setM_date(String m_date) {
		this.m_date = m_date;
	}
	public int getM_people() {
		return m_people;
	}
	public void setM_people(int m_people) {
		this.m_people = m_people;
	}
	public int getM_max() {
		return m_max;
	}
	public void setM_max(int m_max) {
		this.m_max = m_max;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
}
