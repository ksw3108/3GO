package sns.model;

public class SnsBean {
	private int sns_no;
	private String sns_content;
	private String sns_date;
	private int m_no;
	private String sns_like;
	private String sns_hate;
	public int getSns_no() {
		return sns_no;
	}
	public void setSns_no(int sns_no) {
		this.sns_no = sns_no;
	}
	public String getSns_content() {
		return sns_content;
	}
	public void setSns_content(String sns_content) {
		this.sns_content = sns_content;
	}
	public String getSns_date() {
		return sns_date;
	}
	public void setSns_date(String sns_date) {
		this.sns_date = sns_date;
	}
	public int getM_no() {
		return m_no;
	}
	public void setM_no(int m_no) {
		this.m_no = m_no;
	}
	public String getSns_like() {
		return sns_like;
	}
	public void setSns_like(String sns_like) {
		this.sns_like = sns_like;
	}
	public String getSns_hate() {
		return sns_hate;
	}
	public void setSns_hate(String sns_hate) {
		this.sns_hate = sns_hate;
	}

}
